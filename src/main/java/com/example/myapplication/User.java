package com.example.myapplication;

public class User {
    public String id, email, password, status, nam, age, inf, help, problem, phone, cod;

    public User() {

    }

    public User(String id, String email, String password, String status, String nam, String age, String inf, String help, String problem, String phone, String cod) {
        this.id = id;
        this.email = email;
        this.password = password;
        this.status = status;
        this.nam = nam;
        this.age = age;
        this.inf = inf;
        this.help = help;
        this.problem = problem;
        this.phone = phone;
        this.cod = cod;
    }

}
